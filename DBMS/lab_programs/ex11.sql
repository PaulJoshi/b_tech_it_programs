CREATE OR REPLACE PROCEDURE GET_WINNER AS
CURSOR C IS SELECT * FROM VOTED;
C1 NUMBER := 0;
C2 NUMBER := 0;
C3 NUMBER := 0;
C4 NUMBER := 0;
C5 NUMBER := 0;
WINNER NUMBER;
T NUMBER;
NAME VARCHAR2(20);
BEGIN
WINNER := 0;
FOR S IN C LOOP
IF S.CAND_ID = 301 THEN C1 := C1 + 1;
ELSIF S.CAND_ID = 302 THEN C2 := C2 + 1;
ELSIF S.CAND_ID = 303 THEN C3 := C3 + 1;
ELSIF S.CAND_ID = 304 THEN C4 := C4 + 1;
ELSIF S.CAND_ID = 305 THEN C5 := C5 + 1;
END IF;
END LOOP;
IF C1 > C2 AND C1 > C3 AND C1 > C4 AND C1 > C5 THEN WINNER := 301;
ELSIF C2 > C1 AND C2 > C3 AND C2 > C4 AND C2 > C5 THEN WINNER := 302;
ELSIF C3 > C1 AND C3 > C2 AND C3 > C4 AND C3 > C5 THEN WINNER := 303;
ELSIF C4 > C1 AND C4 > C2 AND C4 > C3 AND C4 > C5 THEN WINNER := 304;
ELSE WINNER := 305;
END IF;
T := TOTAL_VOTES(WINNER);
SELECT CAND_NAME INTO NAME FROM CANDIDATE WHERE CAND_ID = WINNER;
DBMS_OUTPUT.PUT_LINE('WINNER IS CANDIDATE : ' || NAME);
DBMS_OUTPUT.PUT_LINE('CANDIDATE ID : ' || WINNER);
DBMS_OUTPUT.PUT_LINE('NUMBER OF VOTES : ' || T);
END;
