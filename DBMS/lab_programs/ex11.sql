CREATE OR REPLACE FUNCTION TOTAL_VOTES(C_ID IN NUMBER) RETURN NUMBER IS
TOTAL NUMBER;
CURSOR C IS SELECT * FROM VOTED;
S C%ROWTYPE;
BEGIN
OPEN C;
TOTAL := 0;
LOOP
FETCH C INTO S;
EXIT WHEN C%NOTFOUND;
IF S.CAND_ID = C_ID THEN TOTAL := TOTAL + 1;
END IF;
END LOOP;
RETURN TOTAL;
CLOSE C;
END;
/

